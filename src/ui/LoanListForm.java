    private void loadCustomerNames() {
        customerNames = new HashMap<>();
        List<Customer> customers = customerService.getAll();
        for (Customer c : customers) {
            customerNames.put(c.getCustomerId(), c.getFullName());
        }
    }

    private void setupTable() {
        String[] columns = {"Loan ID", "Customer", "Type", "Amount", "Status"};
        tableModel = new DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblLoanListForm.setModel(tableModel);
    }

    private void loadLoans() {
        tableModel.setRowCount(0);
        List<Loan> loans = loanService.getAll();
        for (Loan l : loans) {
            String customerName = customerNames.getOrDefault(l.getCustomerId(), "Unknown");
            tableModel.addRow(new Object[]{
                l.getLoanId(),
                customerName,
                l.getLoanType(),
                String.format("%.2f", l.getAmount()),
                l.getStatus()
            });
        }
    }

    private Loan getSelectedLoan() {
        int selectedRow = tblLoanListForm.getSelectedRow();
        if (selectedRow == -1) {
            return null;
        }
        int loanId = (Integer) tableModel.getValueAt(selectedRow, 0);
        List<Loan> loans = loanService.getAll();
        for (Loan l : loans) {
            if (l.getLoanId() == loanId) {
                return l;
            }
        }
        return null;
    }

    public void refreshTable() {
        loadCustomerNames();
        loadLoans();
        // Refresh dashboard statistics if dashboard form is available
        if (dashboardForm != null) {
            dashboardForm.refreshStatistics();
        }
    }

