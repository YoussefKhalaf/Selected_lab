    private void btnApproveLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveLoanActionPerformed
        Loan selected = getSelectedLoan();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a loan to approve", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (loanService.approveLoan(selected.getLoanId())) {
            JOptionPane.showMessageDialog(this, "Loan approved successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to approve loan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnApproveLoanActionPerformed

    private void btnRepayLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRepayLoanActionPerformed
        Loan selected = getSelectedLoan();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a loan to repay", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (loanService.repayLoan(selected.getLoanId())) {
            JOptionPane.showMessageDialog(this, "Loan repaid successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to repay loan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRepayLoanActionPerformed

    private void btnDeleteLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteLoanActionPerformed
        Loan selected = getSelectedLoan();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a loan to delete", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this loan?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            if (loanService.deleteLoan(selected.getLoanId())) {
                JOptionPane.showMessageDialog(this, "Loan deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                refreshTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete loan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteLoanActionPerformed

