    private void setupTable() {
        String[] columns = {"ID", "Name", "Phone", "Email"};
        tableModel = new DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblCustomerList.setModel(tableModel);
    }
    
    private void loadCustomers() {
        try {
            tableModel.setRowCount(0);
            List<Customer> customers = customerService.getAll();
            for (Customer c : customers) {
                tableModel.addRow(new Object[]{
                    c.getCustomerId(),
                    c.getFullName(),
                    c.getPhone(),
                    c.getEmail()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading customers: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private Customer getSelectedCustomer() {
        int selectedRow = tblCustomerList.getSelectedRow();
        if (selectedRow == -1) {
            return null;
        }
        int customerId = (Integer) tableModel.getValueAt(selectedRow, 0);
        List<Customer> customers = customerService.getAll();
        for (Customer c : customers) {
            if (c.getCustomerId() == customerId) {
                return c;
            }
        }
        return null;
    }

