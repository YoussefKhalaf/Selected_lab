    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        Customer selected = getSelectedCustomer();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a customer to edit", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        CustomerForm customerForm = new CustomerForm(this, selected, dashboardForm);
        customerForm.setVisible(true);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String query = txtSearch.getText().trim().toLowerCase();
        if (query.isEmpty()) {
            loadCustomers();
            return;
        }
        
        try {
            tableModel.setRowCount(0);
            List<Customer> customers = customerService.getAll();
            for (Customer c : customers) {
                if (c.getFullName().toLowerCase().contains(query) ||
                    c.getEmail().toLowerCase().contains(query) ||
                    c.getPhone().toLowerCase().contains(query)) {
                    tableModel.addRow(new Object[]{
                        c.getCustomerId(),
                        c.getFullName(),
                        c.getPhone(),
                        c.getEmail()
                    });
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error searching customers: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Customer selected = getSelectedCustomer();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a customer to delete", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Check if customer can be deleted (has no transactions)
        if (!customerService.canDeleteCustomer(selected.getCustomerId())) {
            JOptionPane.showMessageDialog(this, 
                "Cannot delete this customer because they have existing transactions.", 
                "Delete Not Allowed", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete customer: " + selected.getFullName() + "?",
            "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (confirm == JOptionPane.YES_OPTION) {
            if (customerService.delete(selected.getCustomerId())) {
                JOptionPane.showMessageDialog(this, "Customer deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadCustomers();
                // Refresh dashboard statistics if dashboard form is available
                if (dashboardForm != null) {
                    dashboardForm.refreshStatistics();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete customer", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    
    public void refreshTable() {
        loadCustomers();
        // Also refresh dashboard statistics if dashboard form is available
        if (dashboardForm != null) {
            dashboardForm.refreshStatistics();
        }
    }

