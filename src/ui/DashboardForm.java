    private void loadStatistics() {
        try {
            CustomerService customerService = new CustomerService();
            AccountService accountService = new AccountService();
            LoanService loanService = new LoanService();
            
            int totalCustomers = 0;
            int totalAccounts = 0;
            double totalBalance = 0.0;
            int totalLoans = 0;
            
            try {
                totalCustomers = customerService.getAll().size();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            try {
                totalAccounts = accountService.getAll().size();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            try {
                totalBalance = accountService.getTotalBalance();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            try {
                totalLoans = loanService.getAll().size();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            lblTotalCustomers.setText(String.valueOf(totalCustomers));
            lblTotalAccounts.setText(String.valueOf(totalAccounts));
            lblTotalBalance.setText(String.format("%.2f", totalBalance));
            lblTotalLoans.setText(String.valueOf(totalLoans));
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading statistics: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void refreshStatistics() {
        loadStatistics();
    }
    
    private void updateWelcomeMessage() {
        try {
            AuthService authService = AuthService.getInstance();
            if (authService != null && authService.loggedUser != null) {
                lblWelcome.setText("Welcome, " + authService.loggedUser.getFullName());
            } else {
                lblWelcome.setText("Welcome, Guest");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            lblWelcome.setText("Welcome, User");
        }
    }

